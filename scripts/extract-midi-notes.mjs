/**
 * Fetches Bad Romance MIDI, extracts chords with timing, and outputs:
 *   - src/ui/tui/primitives/bad-romance-notes.ts  (chord+timing data for synth)
 *   - scripts/snake-synth.mjs  (self-contained synthesis script, data embedded)
 *
 * Usage: node scripts/extract-midi-notes.mjs
 */

import pkg from 'midi-file';
const { parseMidi } = pkg;
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join, dirname } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const MIDI_URL = 'https://bitmidi.com/uploads/67250.mid';
const CHORD_WINDOW_TICKS = 10;

// ── Fetch + parse ───────────────────────────────────────────────────

console.log('Fetching', MIDI_URL);
const res = await fetch(MIDI_URL);
const buf = Buffer.from(await res.arrayBuffer());
const midi = parseMidi(buf);
const TPB = midi.header.ticksPerBeat;

// ── Tempo map ───────────────────────────────────────────────────────

const tempoMap = [{ tick: 0, uspb: 500000 }];
{
  let tick = 0;
  for (const ev of midi.tracks[0]) {
    tick += ev.deltaTime;
    if (ev.type === 'setTempo') tempoMap.push({ tick, uspb: ev.microsecondsPerBeat });
  }
}

function tickToMs(tick) {
  let ms = 0, prevTick = 0, uspb = 500000;
  for (const entry of tempoMap) {
    if (entry.tick >= tick) break;
    ms += ((entry.tick - prevTick) / TPB) * (uspb / 1000);
    prevTick = entry.tick;
    uspb = entry.uspb;
  }
  ms += ((tick - prevTick) / TPB) * (uspb / 1000);
  return ms;
}

// ── Collect notes with durations ────────────────────────────────────

const active = new Map(); // `${channel}-${note}` -> {startTick, velocity}
const rawNotes = []; // {startTick, endTick, note, velocity}

for (let t = 0; t < midi.tracks.length; t++) {
  let tick = 0;
  for (const ev of midi.tracks[t]) {
    tick += ev.deltaTime;
    const key = `${ev.channel}-${ev.noteNumber}`;
    if (ev.type === 'noteOn' && ev.velocity > 0) {
      active.set(key, { startTick: tick, velocity: ev.velocity });
    } else if (ev.type === 'noteOff' || (ev.type === 'noteOn' && ev.velocity === 0)) {
      const a = active.get(key);
      if (a) {
        rawNotes.push({ startTick: a.startTick, endTick: tick, note: ev.noteNumber, velocity: a.velocity });
        active.delete(key);
      }
    }
  }
}

rawNotes.sort((a, b) => a.startTick - b.startTick);

// ── Group into chords ───────────────────────────────────────────────

const chords = []; // { t: ms, d: ms, n: number[] }
let i = 0;
while (i < rawNotes.length) {
  const base = rawNotes[i].startTick;
  const group = [];
  while (i < rawNotes.length && rawNotes[i].startTick <= base + CHORD_WINDOW_TICKS) {
    group.push(rawNotes[i]);
    i++;
  }
  const notes = [...new Set(group.map((g) => g.note))];
  const avgDur = group.reduce((s, g) => s + (g.endTick - g.startTick), 0) / group.length;
  chords.push({ t: Math.round(tickToMs(base)), d: Math.round(tickToMs(base + avgDur) - tickToMs(base)), n: notes });
}

const totalMs = Math.max(...chords.map((c) => c.t + c.d)) + 500;
console.log(`Chords: ${chords.length}, total duration: ${(totalMs / 1000).toFixed(1)}s`);

// ── Write bad-romance-notes.ts ──────────────────────────────────────

const primaryBpm = Math.round(60_000_000 / tempoMap[0].uspb);

const tsOut = join(ROOT, 'src/ui/tui/primitives/bad-romance-notes.ts');
writeFileSync(tsOut, `\
// Auto-generated by scripts/extract-midi-notes.mjs — do not edit.
// Source: Bad Romance by Lady Gaga (Universal, 2009) via bitmidi.com/uploads/67250.mid
// ${chords.length} chords, ${(totalMs / 1000).toFixed(1)}s total.
export const BPM = ${primaryBpm};
export interface MidiChord { t: number; d: number; n: number[] }
export const BAD_ROMANCE: MidiChord[] = ${JSON.stringify(chords)};
`);
console.log('Wrote', tsOut);

// ── Write snake-synth.mjs ───────────────────────────────────────────
// Self-contained: timing data is embedded, no npm deps needed at runtime.

const synthOut = join(ROOT, 'scripts/snake-synth.mjs');
writeFileSync(synthOut, `\
// AUTO-GENERATED by scripts/extract-midi-notes.mjs — do not edit.
// Synthesizes Bad Romance to a WAV file using pure Node.js.
// Usage: node snake-synth.mjs <output.wav>

import { writeFileSync } from 'node:fs';

const WAV_PATH = process.argv[2];
if (!WAV_PATH) { console.error('Usage: node snake-synth.mjs <output.wav>'); process.exit(1); }

const CHORDS = ${JSON.stringify(chords)};
const TOTAL_MS = ${totalMs};
const SR = 22050;
const totalSamples = Math.ceil(TOTAL_MS / 1000 * SR);
const mix = new Float32Array(totalSamples);

function midiToFreq(n) { return 440 * Math.pow(2, (n - 69) / 12); }

for (const { t, d, n } of CHORDS) {
  const startS = Math.floor(t / 1000 * SR);
  const durS   = Math.max(Math.floor(d / 1000 * SR), 1);
  const rel    = Math.min(Math.floor(SR * 0.08), durS);
  const atk    = Math.min(Math.floor(SR * 0.005), durS);
  const amp    = 0.12 / Math.sqrt(n.length); // scale down for polyphony

  for (const note of n) {
    const freq = midiToFreq(note);
    const end  = Math.min(startS + durS + rel, totalSamples);
    for (let i = startS; i < end; i++) {
      const pos = i - startS;
      const env = Math.min(pos / atk, 1) * (pos < durS ? 1 : (durS + rel - pos) / rel);
      const t_  = pos / SR;
      mix[i] += amp * env * (
        Math.sin(2 * Math.PI * freq       * t_)       +
        Math.sin(2 * Math.PI * freq * 2   * t_) * 0.4 +
        Math.sin(2 * Math.PI * freq * 3   * t_) * 0.2
      );
    }
  }
}

// Normalize to 90% of peak
let peak = 0;
for (const s of mix) if (Math.abs(s) > peak) peak = Math.abs(s);
const scale = peak > 0 ? 0.9 / peak : 1;

// Write 16-bit mono WAV
const dataSize = totalSamples * 2;
const wav = Buffer.alloc(44 + dataSize);
wav.write('RIFF', 0);   wav.writeUInt32LE(36 + dataSize, 4); wav.write('WAVE', 8);
wav.write('fmt ', 12);  wav.writeUInt32LE(16, 16);
wav.writeUInt16LE(1, 20); wav.writeUInt16LE(1, 22);
wav.writeUInt32LE(SR, 24); wav.writeUInt32LE(SR * 2, 28);
wav.writeUInt16LE(2, 32);  wav.writeUInt16LE(16, 34);
wav.write('data', 36);  wav.writeUInt32LE(dataSize, 40);
for (let i = 0; i < totalSamples; i++)
  wav.writeInt16LE(Math.round(Math.max(-32768, Math.min(32767, mix[i] * scale * 32767))), 44 + i * 2);

writeFileSync(WAV_PATH, wav);
console.log('Wrote', WAV_PATH);
`);
console.log('Wrote', synthOut);
